package j08;
   
public class Contoh01 {
    public static void main(String[] args) {
        
        // PERULANGAN FOR
        int x;
        for(x = 1; x <= 5; x++)
        {
            System.out.println("SMK MOKLET");
            
        }
        // PERULANGAN WHILE
        int y = 1;
        while(1 <= 5)
        {
            System.out.println("SMK MOKLET");
            y++;
        }
        
        // PERULANGAN DO WHILE
        int j = 1;
        do
        {
            System.out.println("SMK MOKLET");
            j++;
        }
        while(j <= 5);
        
        }
    }
    

